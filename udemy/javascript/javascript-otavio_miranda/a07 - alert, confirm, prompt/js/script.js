let num1 = Number(prompt('Digite um número: '));    // prompt() é uma função que abre uma caixa de diálogo para o usuário digitar algo
let num2 = Number(prompt('Digite outro número: ')); // Number() é uma função que converte o que o usuário digitou em um número

let resultado = num1 + num2;                        // soma dos números digitados pelo usuário

alert(`O resultado da soma entre os número ${num1} e ${num2} é igual a: ${resultado}.`); 
// alert() é uma função que abre uma caixa de diálogo com uma mensagem para o usuário.
