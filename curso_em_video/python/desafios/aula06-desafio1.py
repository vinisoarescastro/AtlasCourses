# Aula 06 - Desafio 01
# Crie um programa que soma dois números e mostre o resultado.

print('=============== DESAFIO 1 ===============')
nome = input('Digite seu nome: ')
n1 = int(input(f'Olá, {nome}. Digite um número: '))
n2 = int(input('Digite outro número: '))

resultado = n1 + n2

print('============================== RESULTADO ==============================')
print(f'Olá, {nome}. A soma entre os números: {n1}+{n2} é igual a: {resultado}.')
print('=======================================================================')