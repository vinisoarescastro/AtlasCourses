var = input('Digite algo: ')

print('O tipo primitivo desse valor é: ', type(var))
print('É númerico?', var.isnumeric())
print('É alfabetico?', var.isalpha())
print('É somente espaço?', var.isspace())
print('É um número decimal?', var.isdecimal())
print('É minusculo?', var.islower())